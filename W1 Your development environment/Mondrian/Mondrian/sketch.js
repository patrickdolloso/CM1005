function setup()
{
	//create a large square canvas
	createCanvas(800, 800);
}

function draw()
{

	//set the fill colour to red
	fill(255, 0, 0);

	//set a thick stroke weight for the black lines
	strokeWeight(12);

	//draw the red rectangle

	rect(100, 0, 700, 500);
	
	strokeWeight(12);
	line(100, 0, 100, 800);

	fill(0,100,250);
	rect(0, 500, 100, 500);

	fill(0,0,0);
	rect(0,200,100,20)

	fill(0,255,0)
	rect(700,600,200,300)

	line(700,600,700,500)

}